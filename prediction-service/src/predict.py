"""Load a trained model and produce an autoregressive multi-day forecast.

How the forecast works (and its honest limitations):
  1. Take the most recent WINDOW days of real, indicator-enriched data.
  2. Predict the next day's Close with the LSTM.
  3. Append a synthetic candle for that day (Open=High=Low=Close=prediction,
     Volume = recent average) and recompute the indicators.
  4. Slide the window forward and repeat for `horizon` days.

Because future High/Low/Volume are unknown, steps 3-4 are an approximation, so
uncertainty grows with distance. We widen a confidence band accordingly. This
is an educational estimate from past patterns -- NOT financial advice.
"""
import json
from functools import lru_cache
from typing import Optional

import joblib
import numpy as np
import pandas as pd
import torch

from . import config, data_loader, dataset
from .indicators import add_indicators
from .model import StockLSTM


def _model_path(ticker: str):
    return config.MODELS_DIR / f"{ticker}_lstm.pt"


def is_trained(ticker: str) -> bool:
    return _model_path(ticker).exists() and \
        (config.MODELS_DIR / f"{ticker}_scaler.pkl").exists()


@lru_cache(maxsize=64)
def _load_artifacts(ticker: str):
    scaler = joblib.load(config.MODELS_DIR / f"{ticker}_scaler.pkl")
    # n_features is fixed by FEATURES; build model and load weights.
    model = StockLSTM(len(config.FEATURES))
    model.load_state_dict(torch.load(_model_path(ticker), map_location="cpu"))
    model.eval()
    return model, scaler


def _next_business_days(last_date: pd.Timestamp, n: int):
    return pd.bdate_range(start=last_date + pd.Timedelta(days=1), periods=n)


def forecast(ticker: str, horizon: int = config.HORIZON) -> dict:
    """Return history tail + predicted path with a widening confidence band."""
    if not is_trained(ticker):
        raise FileNotFoundError(
            f"No trained model for {ticker}. Run: python -m src.train --ticker {ticker}"
        )

    model, scaler = _load_artifacts(ticker)

    raw = data_loader.load(ticker)              # cached OHLCV
    work = raw.copy()
    avg_vol = float(work["Volume"].tail(20).mean())

    preds = []
    for _ in range(horizon):
        ind = add_indicators(work)
        matrix = dataset.build_matrix(ind)
        window = matrix[-config.WINDOW:]
        scaled = scaler.transform(window)
        x = torch.from_numpy(scaled[np.newaxis, :, :].astype("float32"))
        with torch.no_grad():
            pred_scaled = float(model(x).item())
        pred_price = float(dataset.invert_close(np.array([pred_scaled]), scaler)[0])
        preds.append(pred_price)

        # Append synthetic candle and continue rolling forward.
        next_date = _next_business_days(work.index[-1], 1)[0]
        work.loc[next_date] = {
            "Open": pred_price, "High": pred_price,
            "Low": pred_price, "Close": pred_price, "Volume": avg_vol,
        }

    last_real_close = float(raw["Close"].iloc[-1])
    pred_dates = _next_business_days(raw.index[-1], horizon)

    # Confidence band widens with the square root of the horizon (uncertainty
    # compounds). Base 3% on day 1, scaling outward.
    band = []
    for i, p in enumerate(preds, start=1):
        pct = float(0.03 + 0.012 * np.sqrt(i))
        band.append({"lower": round(float(p) * (1 - pct), 2),
                     "upper": round(float(p) * (1 + pct), 2)})

    hist_tail = raw[["Close"]].tail(120).copy()
    history = [
        {"date": d.strftime("%Y-%m-%d"), "close": round(float(c), 2)}
        for d, c in zip(hist_tail.index, hist_tail["Close"])
    ]
    forecast_path = [
        {"date": d.strftime("%Y-%m-%d"), "predicted": round(p, 2),
         "lower": b["lower"], "upper": b["upper"]}
        for d, p, b in zip(pred_dates, preds, band)
    ]

    metrics = {}
    mpath = config.MODELS_DIR / f"{ticker}_metrics.json"
    if mpath.exists():
        metrics = json.loads(mpath.read_text())

    horizon_change = (preds[-1] - last_real_close) / last_real_close * 100.0
    return {
        "ticker": ticker,
        "display_name": config.DISPLAY_NAMES.get(ticker, ticker),
        "last_close": round(last_real_close, 2),
        "horizon_days": horizon,
        "predicted_final": round(preds[-1], 2),
        "predicted_change_pct": round(horizon_change, 2),
        "history": history,
        "forecast": forecast_path,
        "model_mae_rupees": metrics.get("mae_rupees"),
        "disclaimer": (
            "Educational estimate generated by an LSTM from past price patterns. "
            "Markets are unpredictable. This is NOT financial advice."
        ),
    }


if __name__ == "__main__":
    import sys
    sym = sys.argv[1] if len(sys.argv) > 1 else "RELIANCE.NS"
    out = forecast(sym, horizon=15)
    print(json.dumps({k: v for k, v in out.items() if k not in ("history", "forecast")}, indent=2))
    print("first 3 forecast points:", out["forecast"][:3])
